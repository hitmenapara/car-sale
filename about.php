      <h4>Our Work</h4>
      <p>Car dealerships are the retail outlets for the auto manufacturers.</p>

<p>How they "work" is that they order vehicles from the manufacturer based upon what they believe that they
 will sell that model year and what the public is seeking from them. The manufacturer and the dealer 
 arrange lines of credit that the dealer uses to purchase these models from the manufacturer and dealers
  simply pay back those lines of credit.</p>

<p>Well...not "simply", as some dealers have serious issues with sales and they are unable to make their payments.
   If this continues for too long, then the terms of the lines of credit are renegotiated.
    If it becomes a serious problem, the dealership may close.</p>

<p>Dealers also have to purchase some unpopular models from the manufacturer as they have to carry the full
   model line for that manufacturer during that specific model year. These models are often deeply discounted 
   by the end of the model and then either resold to the manufacturer or sold through third party auctions.
    The key is to keep enough of the models on your lot that will sell, without taking in too many that sell
     poorly or not at all.</p>

<p>Most dealerships really make their money from their service departments. 
  Some mechanical and electrical repairs require dealer-trained mechanics to perform and
   the majority of them work at the dealership. Also vehicles which are leased require that
    their service be done by the dealership and so their lessees have to take them there 
    which is not inexpensive.</p>

<p>Additionally, many parts are OEM (original equipment manufacturer) and can only be purchased through the dealership.
   This means that not only does the customer have to purchase the parts there, 
   all of the private repair shops have to purchase them there as well. 
   The dealer is free to mark up the parts within an MSRP (Manufacturer's Suggested Retail Price) range;
   however most give their best clients a substantial discount that still allows the dealer to profit.</p>
